Extra informatie bij betaling via AfterPay:
<br>
AfterPay voert voor Fashion-click het volledige achteraf betaalproces uit. Dit betekent dat u een digitale factuur van AfterPay via e-mail ontvangt om het gekochte product(en) te betalen. U kunt bij Fashion-click betalen met een digitale factuur tot een bedrag van € 100,- als u voor de eerste keer betaalt via AfterPay. Bent u al bekend bij AfterPay kunt u betalen tot een bedrag van € 250,-.
<br>
Ter goedkeuring van uw verzoek om te betalen voert AfterPay een gegevenscontrole uit. AfterPay hanteert een strikt privacy beleid zoals omschreven in haar privacy statement. Mocht onverhoopt uw verzoek tot betaling niet geautoriseerd worden, dan kunt u natuurlijk het aan te kopen product betalen met een andere betaalmethode.
<br>
U kunt bij vragen altijd contact met AfterPay opnemen. Voor meer informatie verwijzen wij u naar het consumentendeel van de AfterPay website.