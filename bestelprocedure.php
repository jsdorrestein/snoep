Wanneer u een bestelling wilt plaatsen gaat u als volgt te werk: <br>
<br>
Fase 1 Plaats de producten in de winkelwagen <br>

Fase 2 Zodra u de gewenste producten heeft toegevoegd gaat u naar de winkelwagen en kiest u voor "ga door met afrekenen". <br>

Fase 3 U komt nu in een nieuw scherm waar u uw gegevens kunt invullen. DIt gebeurt in een veilige omgeving. Doorloop hier de 4 stappen: <br>
<br>
- Stap 1: vul uw adresgegevens in <br>
- Stap 2: kies een verzendwijze <br>
- Stap 3: kiez een betaalwijze <br>
- Stap 4: Controleer uw bestelling. Iets vergeten? Kies dan voor de link "ga terug naar uw winkelwagen" <br>
<br>
Wanneer u akkoord bent met de bestelling en de algemene voorwaarden vinkt u "ik ga akkoord met de algemene voorwaarden" aan en drukt u op de knop "plaats bestelling". <br>
Let op dat u zich nog tot het laatste moment kunt bedenken. Mocht u zich echter hierna nog bedenken dan kunt u nog gebruik maken van het herroepingsrecht. Wij adviseren u dan om z.s.m. contact met ons op te nemen zodat de goederen niet uit worden gestuurd. <br>
Zodra uw bestelling is geplaatst doen we er alles aan om uw goederen zo spoedig mogelijk te leveren.